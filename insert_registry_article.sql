<h2>KidneySphere Registry 是什么？</h2>

<p>KidneySphere Registry 是一个聚焦肾脏病领域的数字化科研登记与随访平台。</p>

<p>它服务的，不只是单个课题的数据录入，而是整个研究协作过程中的关键环节：病例登记、结构化数据采集、长期随访、跨中心协作，以及未来更大范围的真实世界研究基础建设。</p>

<p>现阶段，平台重点支持以下几类场景：</p>

<h3>多中心登记研究</h3>
<p>围绕肾小球疾病、慢性肾病、糖尿病肾病、高血压、透析、肾移植及部分罕见肾病，支持更规范地建立多中心研究队列。</p>

<h3>真实世界研究</h3>
<p>帮助研究团队持续积累临床诊疗过程、随访信息与结局指标，为更高质量的真实世界研究提供底层支持。</p>

<h3>长期随访管理</h3>
